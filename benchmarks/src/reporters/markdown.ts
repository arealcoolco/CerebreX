/**
 * CerebreX Benchmark Markdown Reporter
 */

import fs from 'fs';
import path from 'path';
import type { BenchmarkResult, BenchmarkSuite } from '../types.js';

function fmt(n: number, unit = 'ms'): string {
  return `${n.toFixed(2)}${unit}`;
}

function resultRow(r: BenchmarkResult): string {
  const status = r.successRate >= 0.99 ? 'pass' : r.successRate >= 0.90 ? 'warn' : 'fail';
  const icon = status === 'pass' ? 'OK' : status === 'warn' ? 'WARN' : 'FAIL';
  return `| ${icon} | ${r.name} | ${fmt(r.latency.p50)} | ${fmt(r.latency.p95)} | ${fmt(r.latency.p99)} | ${fmt(r.latency.mean)} | ${r.throughputOpsPerSec.toFixed(1)} | ${(r.successRate * 100).toFixed(1)}% |`;
}

export function toMarkdown(suite: BenchmarkSuite): string {
  const lines: string[] = [];
  lines.push(`# CerebreX Benchmark Report — ${suite.name}`);
  lines.push('');
  lines.push(`**Run at:** ${suite.runAt}  `);
  lines.push(`**Duration:** ${(suite.totalDurationMs / 1000).toFixed(2)}s  `);
  lines.push(`**Platform:** ${suite.environment.platform} / ${suite.environment.runtime} / ${suite.environment.cpuCores} CPUs`);
  lines.push('');
  lines.push('## Results');
  lines.push('');
  lines.push('| Status | Benchmark | p50 | p95 | p99 | Mean | Ops/s | Success |');
  lines.push('|--------|-----------|-----|-----|-----|------|-------|---------|');
  for (const r of suite.results) {
    lines.push(resultRow(r));
  }
  lines.push('');

  const total = suite.results.length;
  const passed = suite.results.filter((r) => r.successRate >= 0.95).length;
  const avgP95 = suite.results.reduce((a, r) => a + r.latency.p95, 0) / total;
  lines.push('## Summary');
  lines.push('');
  lines.push(`- Benchmarks passing (>=95% success): **${passed}/${total}**`);
  lines.push(`- Average p95 latency: **${fmt(avgP95)}**`);
  lines.push('');
  lines.push('---');
  lines.push('*Generated by CerebreX benchmark suite*');
  return lines.join('\n');
}

export function writeMarkdown(suite: BenchmarkSuite, outPath: string): void {
  const dir = path.dirname(outPath);
  if (dir && dir !== '.') fs.mkdirSync(dir, { recursive: true });
  fs.writeFileSync(outPath, toMarkdown(suite));
  console.log(`Markdown report written to ${outPath}`);
}
